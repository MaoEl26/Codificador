    private void algoritmoNuevoBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_algoritmoNuevoBtnActionPerformed

        JFileChooser fc = new JFileChooser();
        int returnVal = fc.showSaveDialog(this);
        String url = "E:/Documentos/TEC/Dise√±o/Actividades/Act04/Codificador/Codificador/src/Model/";
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = fc.getSelectedFile();
            try {
                Files.move(Paths.get(file.getAbsolutePath()), Paths.get(url+file.getName()), StandardCopyOption.REPLACE_EXISTING);
            } catch (IOException ex) {
                Logger.getLogger(Administrador.class.getName()).log(Level.SEVERE, null, ex);
            }
            int largo = file.getName().length();
            String nombre = file.getName().substring(3, largo-5);
            System.out.println(nombre);
            cargarArchivo("Model.alg"+nombre);
            algoritmosCombo.addItem(nombre);
        } else {
            System.out.println("cancel");
        }
    }//GEN-LAST:event_algoritmoNuevoBtnActionPerformed

